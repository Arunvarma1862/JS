// switch statement

let day = 3;

switch (day) {
  case 0:
    console.log("sunday");
    break;
  case 1:
    console.log("monday");
    break;
  case 2:
    console.log("tuesday");
    break;
  case 3:
    console.log("wednesday");
    break;
  case 4:
    console.log("Thrusday");
    break;
  case 5:
    console.log("friday");
    break;
  default:
    console.log("saturday");
    break;
}
